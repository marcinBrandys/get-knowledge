const mongooseUserModel = require('mongoose');
const userSchema = mongooseUserModel.Schema({
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: false
    },
    email: {
        type: String,
        required: true,
        unique: true,
        trim: true
    },
    password: {
        type: String,
        required: true,
        select: false
    }
});

module.exports = mongooseUserModel.model('user', userSchema);
